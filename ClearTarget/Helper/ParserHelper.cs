        public static async Task ReadGoogleCESApis()
        {
            await Task.Run(() =>
            {
                using (FileStream fs = new FileStream(Path.Combine(AppDomain.CurrentDomain.BaseDirectory, "Files", "GoogleCESApis.txt"), FileMode.Open, FileAccess.Read))
                using (StreamReader reader = new StreamReader(fs))
                {
                    string line;
                    while ((line = reader.ReadLine()) != null)
                    {
                        GoogleCESAPIs.Add(line);
                    }
                }
            });
            
        }
